import { Link, useNavigate } from "react-router";
import { useState } from "react";
import { Mail, Lock } from "lucide-react";
import { motion } from "motion/react";
import { apiPost } from "../lib/api";
import { useAuthStore } from "../auth.store";

// type LoginResponse = {
